class Buku{
	
	private String judul;
	private String pengarang;
	
	Buku(){
	}
	
	Buku(String j, String p){
		this.judul = j;
		this.pengarang = p;
	}
	
	void setJudul(String j){
		this.judul = j;
	}
	void setPengarang(String p){
		this.pengarang = p;
	}
	
	String getJudul(){
		return this.judul;
	}
	
	String getPengarang(){
		return this.pengarang;
	}
	
}