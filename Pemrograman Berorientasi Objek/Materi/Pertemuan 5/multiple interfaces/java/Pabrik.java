class Pabrik{
	
	private int kodePabrik;
	private String namaPabrik;
	private String alamatPabrik;
	private String teleponPabrik;
	
	Pabrik(){
	}
	
	public void setKodePabrik(int kodePabrik){
		this.kodePabrik = kodePabrik; 
	}
	
	public int getKodePabrik(){
		return this.kodePabrik;
	}
	
	public void setNamaPabrik(String namaPabrik){
		this.namaPabrik = namaPabrik;
	}
	
	public String getNamaPabrik(){
		return this.namaPabrik;
	}
	
	public void setAlamatPabrik(String alamatPabrik){
		this.alamatPabrik = alamatPabrik;
	}
	
	public String getAlamatPabrik(){
		return this.alamatPabrik;
	}
	
	public void setTeleponPabrik(String teleponPabrik){
		this.teleponPabrik = teleponPabrik;
	}
	
	public String getTeleponPabrik(){
		return this.teleponPabrik;
	}
	
	
}